package com.smukkidi.javavariables;

public class VariablesInJava{
    public static void main(String[] args) {
       // Variables are containers to storing data values

       // In java there are different types of variables to store data
       // Syntax: type variableName = value; Where type is one of data type like "int", "String"
       // Variable name is the name of the variable
       
      // String : String stores the text like "Sreenivasulu Reddy" String values surrounded by the double quotes. 
      String name = "Srinivasulu Reddy";
      System.out.println();
      System.out.println("*********************");
      System.out.println(name);

      int num; 
      num = 16;
      System.out.println(num);

      // Other types
      float myfloat = 12.3f; System.out.println(myfloat);
      double myDouble = 342.56; System.out.println(myDouble);
      char myChar = 'M'; System.out.println(myChar);
      boolean myBool = true; System.out.println(myBool);

      System.out.println("*********************");
      System.out.println();

    }
    
}
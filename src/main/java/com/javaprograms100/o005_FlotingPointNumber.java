package com.javaprograms100;

public class o005_FlotingPointNumber {
    public static void main(String[] args) {
        // float data type
        // float: 4 bytes(32 bits)

        float f = 20.00f;
        System.out.println("Float f = " + f);

        // double data type 
        // double: 8 bytes (64 bits)
        
        double d = 234.00;
        System.out.println("Double d = " + d);

    }

}
